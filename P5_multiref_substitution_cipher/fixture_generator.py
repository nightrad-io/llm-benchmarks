def substitution_encrypt(plaintext: str, key: dict) -> str:
    return "".join(key.get(c, c) for c in plaintext)


def substitution_decrypt(ciphertext: str, key: dict) -> str:
    inverse = {v: k for k, v in key.items()}
    return "".join(inverse.get(c, c) for c in ciphertext)


