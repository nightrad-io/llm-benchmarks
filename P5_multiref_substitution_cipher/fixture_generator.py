def sorted_profile_distance(text_a: str, text_b: str) -> float:
    """Alphabet-shape comparison: rank-ordered frequency profiles, so this
    works even though the ciphertext's literal letters don't correspond to
    Spanish letters (it's a substitution cipher)."""
    def profile(t):
        n = len(t)
        counts = Counter(t)
        return sorted((c / n for c in counts.values()), reverse=True)
    pa, pb = profile(text_a), profile(text_b)
    n = min(len(pa), len(pb))
    return sum((pa[i] - pb[i]) ** 2 for i in range(n))


